public class IfElseIfElse2 {
    public static void main(String[] args) {
        int a = 3;
        if (a > 2) {
            System.out.println("a is greater than 2");
        } else if (a > 1) {
            System.out.println("a is greater than 1");
        } else {
            System.out.println("a is not greater than 1");
        }
    }
}